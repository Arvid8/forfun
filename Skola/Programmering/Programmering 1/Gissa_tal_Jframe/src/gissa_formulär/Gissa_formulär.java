    private void button_StartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_StartActionPerformed
    // Skapa datorns tal genom att hämta det största talet från listrutan, 
    // översätt det till ett heltal och skapa ett slumptal som ligger i 
    // intervallet 1 till det största talet. 
    String störstaTal = ComboBox.getSelectedItem().toString();
    int max = Integer.valueOf(störstaTal); 
    java.util.Random slump = new java.util.Random(); 
    datornsTal = slump.nextInt(max) + 1; 
    
    // Avaktivera knappen btnStartaSpe1et och listrutan listMax. 
    button_Start.setEnabled(false); 
    ComboBox.setEnabled(false) ; 
    // Aktivera textrutan txfGissa och knappen btnGissa. 
    txtf_Gissa.setEnabled(true) ; 
    button_Gissa.setEnabled(true) ; 
    
    // Skriv ?? i IblDatornsTa1. 
    txtf_DatorTal.setText( "??");
    // Sätt variablen antalGissningar till e. 
    antalGissningar = 0;
    }//GEN-LAST:event_button_StartActionPerformed

