    private void button_GissaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_GissaActionPerformed
    antalGissningar++; 
    String gissa = txtf_Gissa.getText(); 
    int gissatTal = Integer.valueOf( gissa ) ; 
    
    
    if ( gissatTal == datornsTal ){
    txtf_Resultat.setText ( "Korrekt efter "+antalGissningar+" försök.");
    txtf_Gissa.setEnabled( false ); 
    button_Gissa.setEnabled( false ); 
    button_Igen.setEnabled( true ); 
    
    txta_MinaResultat.append( antalGissningar + " försök\n");
    }
    
    else if ( gissatTal < datornsTal ){
    txtf_Resultat.setText( "För lågt. Försök igen.");
    }
    else {
    txtf_Resultat.setText( "För högt. Försök igen." ); 
        }
        // Koden fortsätter med main etc. 
    }//GEN-LAST:event_button_GissaActionPerformed

    private void button_IgenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_IgenActionPerformed
    String störstaTal = ComboBox.getSelectedItem().toString();
    int max = Integer.valueOf(störstaTal); 
    java.util.Random slump = new java.util.Random(); 
    datornsTal = slump.nextInt(max) + 1;  
    
    txtf_Gissa.setEnabled(true);
    button_Gissa.setEnabled(true);
    button_Igen.setEnabled(false);
    antalGissningar = 0;
    
    txtf_Gissa.setText(null);
    txtf_Resultat.setText(null);
    txtf_DatorTal.setText( "??");
    
    }//GEN-LAST:event_button_IgenActionPerformed

